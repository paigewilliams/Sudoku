import { Game } from './../src/game.js';

describe('Game', function(){
  var game;
  var failBoard = [[6,9,9,5,7,4,1,8,2],[5,9,1,8,2,9,3,7,6],[7,8,2,6,1,3,9,5,4],[1,9,8,4,6,7,5,2,3],[3,6,5,9,8,2,4,1,7],[4,2,7,1,3,5,8,6,9],[9,5,6,7,4,8,2,3,1],[8,1,3,2,9,6,7,4,5],[2,7,4,3,5,1,6,9,8]];

  beforeEach(function() {
    game = new Game(failBoard)
  });

  it('should contain a board', function(){
    game = new Game([[6,3,9,5,7,4,1,8,2],[5,4,1,8,2,9,3,7,6],[7,8,2,6,1,3,9,5,4],[1,9,8,4,6,7,5,2,3],[3,6,5,9,8,2,4,1,7],[4,2,7,1,3,5,8,6,9],[9,5,6,7,4,8,2,3,1],[8,1,3,2,9,6,7,4,5],[2,7,4,3,5,1,6,9,8]]);
    expect(game.board).toEqual([[6,3,9,5,7,4,1,8,2],[5,4,1,8,2,9,3,7,6],[7,8,2,6,1,3,9,5,4],[1,9,8,4,6,7,5,2,3],[3,6,5,9,8,2,4,1,7],[4,2,7,1,3,5,8,6,9],[9,5,6,7,4,8,2,3,1],[8,1,3,2,9,6,7,4,5],[2,7,4,3,5,1,6,9,8]]);
  });
});

